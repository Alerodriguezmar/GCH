public class Personal {
    
    private int idPersonal;
    private String nombrePersonal;
    private String apellidoPersonal;
    private int identificacionPersonal;
    private int CelularPersonal;
    private String DirrecionPersonal;    
    private int TelFijoPersonal;
    private String TipoSangrePersonal;
    private String Especialidad;
    private int ReThus;

    public Personal() {
    }

    public int getIdPersonal() {
        return idPersonal;
    }

    public void setIdPersonal(int idPersonal) {
        this.idPersonal = idPersonal;
    }

    public String getNombrePersonal() {
        return nombrePersonal;
    }

    public void setNombrePersonal(String nombrePersonal) {
        this.nombrePersonal = nombrePersonal;
    }

    public String getApellidoPersonal() {
        return apellidoPersonal;
    }

    public void setApellidoPersonal(String apellidoPersonal) {
        this.apellidoPersonal = apellidoPersonal;
    }

    public int getIdentificacionPersonal() {
        return identificacionPersonal;
    }

    public void setIdentificacionPersonal(int identificacionPersonal) {
        this.identificacionPersonal = identificacionPersonal;
    }

    public int getCelularPersonal() {
        return CelularPersonal;
    }

    public void setCelularPersonal(int CelularPersonal) {
        this.CelularPersonal = CelularPersonal;
    }

    public String getDirrecionPersonal() {
        return DirrecionPersonal;
    }

    public void setDirrecionPersonal(String DirrecionPersonal) {
        this.DirrecionPersonal = DirrecionPersonal;
    }

    public int getTelFijoPersonal() {
        return TelFijoPersonal;
    }

    public void setTelFijoPersonal(int TelFijoPersonal) {
        this.TelFijoPersonal = TelFijoPersonal;
    }

    public String getTipoSangrePersonal() {
        return TipoSangrePersonal;
    }

    public void setTipoSangrePersonal(String TipoSangrePersonal) {
        this.TipoSangrePersonal = TipoSangrePersonal;
    }

    public String getEspecialidad() {
        return Especialidad;
    }

    public void setEspecialidad(String Especialidad) {
        this.Especialidad = Especialidad;
    }

    public int getReThus() {
        return ReThus;
    }

    public void setReThus(int ReThus) {
        this.ReThus = ReThus;
    }
    
    
    
    
}
