package Entidad;

/**
 *
 * @author Diego
 */
public class Pabellon {
    
    private int Pabellon;
    private String nombrePabellon;
    private String descripcionPabellon;

    public Pabellon() {
    }

    public int getPabellon() {
        return Pabellon;
    }

    public void setPabellon(int Pabellon) {
        this.Pabellon = Pabellon;
    }

    public String getNombrePabellon() {
        return nombrePabellon;
    }

    public void setNombrePabellon(String nombrePabellon) {
        this.nombrePabellon = nombrePabellon;
    }

    public String getDescripcionPabellon() {
        return descripcionPabellon;
    }

    public void setDescripcionPabellon(String descripcionPabellon) {
        this.descripcionPabellon = descripcionPabellon;
    }
    
    
}
